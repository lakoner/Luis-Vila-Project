            <h2>Lluís Vilà i Vendrell </h2>
            <p>(Banyoles, 15 de juny de 1952 - 30 de març de 2010)<br><br>
            Fou un artista català amb una àmplia trajectòria en pintura, escultura i disseny.
            Destaca el seu treball dins els corrents conceptuals de l'"eat art" i l'"art fungible".
            El 1984 va fer una exposició a l'Espai 10 de la Fundació Joan Miró, on les obres estaven situades entre
            la tradició medieval de les escultures votives fetes amb massa de pa i una filiació estètica amb el kitsch
            del barroc popular. La seva traducció d'una artesania popular al camp de l'art contemporani no estava
            exempta d'una estratègia conceptual antimercat: les escultures, efímeres, s'anaven deteriorant al llarg del
            temps d'exposició, com una forma de pop anticonsumista.
            </p><br>
            <p>Inicia l'activitat artística a principis dels anys 70 participant en la Mostra Col·lectiva del Grup Tint-1,
            a la llotja del Tint de Banyoles. Des dels seus inicis fins a mitjans dels anys 80 treballa en el camp de
            la investigació i la instal·lació dins els corrents conceptuals de l'"eat art" i l'art fungible.
            Realitza treballs com Les sabates comestibles (1979), participa a Art 14'83 CIAC a Basilea,
            Entre pa enlluït (1984) a la Fundació Miró de Barcelona, Fagocotosi (1985) a la Sala Montcada de la
            Fundació La Caixa de Barcelona i Cambio de Piel (1985) als festivals de Navarra.</p>
