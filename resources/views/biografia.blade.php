             Realitza Clástics, intervenció en la remodelació del volcà Croscat a Olot, premi FAD (1994), i exposa Capitals
              del Disseny en el Centre George Pompidou de París, i a l'exposició El moble català des del segle XV ,
              al Palau Robert.
            </p><br>
            <p>
            Una constant en la seva obra és la utilització de materials fungibles i la seva crítica dirigida a la societat de
            consum, que dóna per acabada amb l'exposició Àlef (1996), treball caracteritzat en el fet d'utilitzar com a pintura
            la seva pròpia sang, a la galeria Alter Ego de Barcelona. A partir d'aquest moment integra el llenguatge pictòric
            i escultòric en una sola cosa, creant la sèrie Navegants 1995-1997, la sèrie Contraris(1995) que continua amb Logos (2001).
            Un dels treballs més recents es Nautes (2006), una obra que s'acosta cada vegada més a l'art conceptual, on l'artista
            reflexiona amb un to irònic, paradoxal i crític, realitzant escultures i pintures de cos infantil renunciant a la seva
            iconografía de cos habitual, cossos que volen esmenar l'error primordial de què ens parla Antonin Artaud.
            </p>

