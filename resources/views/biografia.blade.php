            <h2>Obra Creativa </h2><br>
            <p>
            El arte de Lluís Vilà es un arte que pasa por el estómago. Las referencias a la comida, como paradigma del consumo,
            lo llevan a tener una posición crítica del mundo que le rodea. Formó parte de un colectivo multidisciplinario
            integrado por escultores, diseñadores, arquitectos e intelectuales, llamado Grupo Tint-2. Mediante un discurso
            de raíz anti-capitalista, el grupo focalizó su interés en cuestionar los gustos y las conductas de las clases
            dominantes y las políticas culturales oficiales. El arte de este artista tan atípico, ha sido relacionado con
            el llamado "eat art", que en otros tiempos practicaron artistas como Antoni Miralda o Daniel Spoerri.
            A diferencia de ellos, el rito nutricio es de orden mental; no es un placer, sino un sufrimiento inevitable.
            </p><br>
            <p>
            Poco a poco, Lluís Vilà ha ido evolucionando hacia esculturas mixtas hechas en hierro y elementos comestibles,
            plastificadas en resina de poliéster transparente. Poco más tarde aparecen las latas de conserva gigantescas,
            hechas a medida de una persona de pie y que inician el verdadero camino hacia la denuncia consumista.
            Un mundo enlatado que dará paso a diferentes instalaciones respecto. Las esculturas laminares son como finísimas
            rebanadas de pan, cinceladas por diferentes signos, figuras y escritura que pese a ser constituidas de un material
            duro como el acero oxidado, su imagen final es blanda como si fuera materia orgánica. Terminando con la serie
            Atman, esculturas laminares de acero de mayor espesor, en un nuevo intento de buscar este hombre nuevo dentro
            de nuestra conciencia tal como nos propone el escritor y pensador estadounidense Ken Wilber.
            </p><br>
            <p>
            Lluís Vilà hace un viaje a los rincones más insondables del cuerpo humano y el resultado es un trabajo donde se
            funden trascendencia y trivialidad, el mundo sublime de lo banal, el placer y el asco, la comida artesano y el
            fast-food.<br><br>
            A partir de 2007 trabaja con la serie "Absolut", trabajo pictórico. En palabras de un crítico de arte:
            </p><br>
            <span class="p-center"><p><b>« El suyo es un "absoluto" que no apela a la tediosa y omnipresente mística de
                manual ni, de hecho, el pesado concepto acuñado sobre roca dura para Hegel: es un" absoluto "que aparece
                después de deconstruir su propia obra , ..., Lluís Vilà fiel a su dejo irónico, utiliza la imagen de
                Pollock pero invirtiéndose la: si él hacía dripping, el artista de Banyoles hace "undripping" »</b></p></span>
            <p><i>Eudald Camps</i></p>
