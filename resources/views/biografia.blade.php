            <h2>Obra Creativa </h2><br>
            <p>
            L'art de Lluís Vilà es un art que passa per l'estómac. Les referències al menjar, com a paradigma del consum,
            el duen a tenir una posició crítica del món que l'envolta. Va formar part d'un col·lectiu multidisciplinari
            integrat per escultors, dissenyadors, arquitectes i intel·lectuals, anomenat Grup Tint-2. Mitjançant un discurs
            d’arrel anticapitalista, el grup va focalitzar el seu interès en qüestionar els gustos i les conductes de les
            classes dominants i les polítiques culturals oficials. L'art d'aquest artista tan atípic, ha estat relacionat
            amb l'anomenat "eat art", que en altres temps practicaren artistes com Antoni Miralda o Daniel Spoerri.
            A diferencia d'ells, el ritu nutrici és d'ordre mental; no és un plaer, sinó un patiment inevitable.
            </p><br>
            <p>
            A poc a poc, Lluís Vilà ha anat evolucionant cap a escultures mixtes fetes en ferro i elements menjables,
            plastificades en resina de poliester transparent. Poc més tard apareixen les llaunes de conserva gegantines,
            fetes a mida d'una persona a peu dret i que inicien el veritable camí cap a la denúncia consumista.
            Un món enllaunat que donarà pas a diferents instal·lacions sobre aquest tema. Les escultures laminars son com
            finíssimes llesques de pa, cisellades per diferents signes, figures i escriptura que malgrat ésser
            constituïdes d'un material dur com l'acer oxidat, la seva imatge final és tova com si fos matèria orgànica.
            Acabant amb la sèrie Atman, escultures laminars d'acer de més gruix, en un nou intent de buscar aquest home
            nou dins la nostra consciència tal com ens proposa l'escriptor i pensador nord-americà Ken Wilber.
            </p><br>
            <p>
            Lluís Vilà fa un viatge als racons més insondables del cos humà i el resultat es un treball on es fonen
            transcendència i trivialitat, el món sublim del banal, el plaer i el fàstic, el menjar artesà i el fast-food.<br><br>
            - A partir del 2007 treballa amb la sèrie "Absolut", treball pictòric. En paraules d'un crític d'art:
            </p><br>
            <span class="p-center"><p><b>« el seu és un "absolut" que no apel·la a la tediosa i omnipresent mística de manual ni, de fet,
            al feixuc concepte encunyat sobre roca dura per Hegel: és un "absolut" que apareix després de desconstruir
            la seva pròpia obra, ..., en Lluís Vilà fidel al seu deix irònic, utilitza la imatge de Pollock però
            invertint-la: si ell feia dripping, l'artista de Banyoles fa " undripping " »</b></p></span>
            <p><i>Eudald Camps</i></p>
