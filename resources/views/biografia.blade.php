            <h2>Lluís Vilà i Vendrell </h2>
            <p>(Banyoles, 15 de junio de 1952 - 30 de marzo de 2010)<br><br>
            Fue un artista catalán con una amplia trayectoria en pintura, escultura y diseño.
            Destaca su trabajo dentro de las corrientes conceptuales del "eat arte" y el "arte fungible".
            En 1984 hizo una exposición en el Espacio 10 de la Fundación Joan Miró, donde las obras estaban situadas
            entre la tradición medieval de las esculturas votivas hechas con masa de pan y una filiación estética con
            el kitsch del barroco popular. Su traducción de una artesanía popular en el campo del arte contemporáneo
            no estaba exenta de una estrategia conceptual antimercado: las esculturas, efímeras, se iban deteriorando
            a lo largo del tiempo de exposición, como una forma de pop anticonsumista.
            </p><br>
            <p>Inicia la actividad artística a principios de los años 70 participando en la Muestra Colectiva del Grupo
            Tint-1, en el palco del Tint de Banyoles. Desde sus inicios hasta mediados de los años 80 trabaja en el
            campo de la investigación y la instalación dentro de las corrientes conceptuales del "eat art" y el
            arte fungible. Realiza trabajos como Los zapatos comestibles (1979), participa en Arte 14'83 CIAC en
            Basilea, Entre pa enlluït (1984) en la Fundación Miró de Barcelona, Fagocotosi (1985) en la Sala
            Montcada de la Fundación La Caixa de Barcelona y Cambio de Piel (1985) en los festivales de Navarra.</p>
        </div>
    </div>

        <div class="p-all-wide">
            <p>
            Poco a poco irá dejando las referencias directas a los elementos comestibles para referirse a ella indirectamente
            desde nuevos materiales como el hierro y la pintura sobre papel. En esta nueva etapa se interesa por la envoltura
            de la comida en conserva, es decir, las latas, y trabaja esculturas en hierro sin volumen, que presenta en
            la Sala Vinçon de Barcelona (1990) y en la Sala Tránsito de Madrid (1991 ).
            </p><br>
            <p>
            Se presenta en diversas ferias internacionales, participa en el pabellón de España para la Expo de Sevilla (1992).
            Realiza Clástics, intervención en la remodelación del volcán Croscat en Olot, premio FAD (1994), y expone Capitales
            del Diseño en el Centro Pompidou de Paris, y en la exposición El moble català desde el segle XV, en el Palau Robert.
            </p><br>
            <p>
            Una constante en su obra es la utilización de materiales fungibles y su crítica dirigida a la sociedad de consumo,
            que da por terminada con la exposición Alef (1996), trabajo caracterizado en el hecho de utilizar como pintura su
            propia sangre, en la galería Alter Ego de Barcelona. A partir de este momento integra el lenguaje pictórico y
            escultórico en una sola cosa, creando la serie Navegants 1995-1997, la serie Contraris (1995) que continúa con
            Logos (2001). Uno de los trabajos más recientes es Nautes (2006), una obra que se acerca cada vez más al arte
            conceptual, donde el artista reflexiona con un tono irónico, paradójico y crítico, realizando esculturas y
            pinturas de cuerpo infantil renunciando a la su iconografía de cuerpo habitual, cuerpos que quieren enmendar
            el error primordial del que nos habla Antonin Artaud.
            </p>

